package SW_Expert_Academy.p2019더블더블;


import java.util.Scanner;

public class Solution {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t=sc.nextInt();
        int sum=1;
        System.out.print(sum+" ");
        for(int i=0; i<t;i++){
            sum*=2;
            System.out.print(sum+" ");

        }



    }

}
