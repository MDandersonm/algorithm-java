package SW_Expert_Academy.p1545거꾸로출력해보아요;


import java.util.Scanner;

public class Solution {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t=sc.nextInt();
        for(int i=t; i>=0; i--){
            System.out.print(i+" ");

        }


    }
}
